    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Base.openTransaction();
        Articulo nuevo = Articulo.createIt(
            "nombre", "",
            "marca", "",
            "stock_actual", 0,
            "stock_minimo", 0,
            "precio_compra",new BigDecimal( monto.getText()),
            "precio_venta",  BigDecimal.ZERO,
            "equivalencia_1", "",
            "equivalencia_2", "",
            "equivalencia_3", "",
            "descripcion", descripcion.getText(),
            "es_articulo",0);

        Base.commitTransaction();

        Articulo p = Articulo.findFirst("id = ?", nuevo.getId());
        Object cols[] = new Object[7];
        cols[0] =p.getId();
        cols[1] = BigDecimal.valueOf(1).setScale(2, RoundingMode.CEILING);
        cols[2] = p.get("codigo");
        cols[3] = p.get("descripcion");
        cols[4] = BigDecimal.valueOf(p.getFloat("precio_compra")).setScale(2, RoundingMode.CEILING);
        cols[5] = p.getBigDecimal("precio_venta");
        cols[6] = BigDecimal.valueOf(p.getFloat("precio_compra")).setScale(2, RoundingMode.CEILING);

        control.getCompraGui().getTablaCompraDefault().addRow(cols);
        control.setCellEditor();
        control.actualizarPrecio();
        this.dispose();

        

    }//GEN-LAST:event_jButton1ActionPerformed

